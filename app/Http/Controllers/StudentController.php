     */
    public function index()
    {
        $students = Student::on()
            ->select([
                'students.*',
                'groups.name as group'
            ])
            ->join(
                'groups',
                'students.group_id',
                '=',
                'groups.id'
            )
            ->paginate(12);

        return view('students/index', ['students' => $students]);
    }

